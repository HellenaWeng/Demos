package org.na.entity;

public class AncientPrescriptionTreats {
    private Integer ancientPrescriptionID;
    private String treatName;

    public Integer getAncientPrescriptionID() {
        return ancientPrescriptionID;
    }

    public void setAncientPrescriptionID(Integer ancientPrescriptionID) {
        this.ancientPrescriptionID = ancientPrescriptionID;
    }

    public String getTreatName() {
        return treatName;
    }

    public void setTreatName(String treatName) {
        this.treatName = treatName;
    }
}
